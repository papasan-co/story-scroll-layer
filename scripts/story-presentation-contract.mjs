import { createHash } from 'node:crypto'
import { cp, mkdir, readFile, rm, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { compileFromFile } from 'json-schema-to-typescript'

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const contractRoot = resolve(repositoryRoot, 'contracts/story-presentation')
const generatedPath = resolve(repositoryRoot, 'app/types/storytime/presentation.generated.ts')
const generatedMotionCapabilitiesPath = resolve(
  repositoryRoot,
  'app/types/storytime/motionCapabilities.generated.ts',
)

async function motionRegistry() {
  const source = await readFile(resolve(contractRoot, 'motion-capabilities.json'))

  return {
    hash: createHash('sha256').update(source).digest('hex'),
    value: JSON.parse(source.toString('utf8')),
  }
}

async function generate() {
  const version = JSON.parse(await readFile(resolve(contractRoot, 'version.json'), 'utf8')).version
  const options = {
    additionalProperties: true,
    bannerComment: [
      '/**',
      ' * Generated from contracts/story-presentation/*.schema.json.',
      ' * Do not edit this file by hand. Run `pnpm contracts:generate`.',
      ' */',
    ].join('\n'),
    style: {
      bracketSpacing: true,
      printWidth: 100,
      semi: false,
      singleQuote: true,
      tabWidth: 2,
      trailingComma: 'all',
      useTabs: false,
    },
  }
  const storyTypes = await compileFromFile(
    resolve(contractRoot, 'story-presentation.schema.json'),
    options,
  )
  const sceneTypes = await compileFromFile(
    resolve(contractRoot, 'scene-presentation.schema.json'),
    { ...options, bannerComment: '' },
  )
  const output = [
    storyTypes.trim(),
    '',
    sceneTypes.trim(),
    '',
    `export const STORY_PRESENTATION_CONTRACT_VERSION = '${version}' as const`,
    '',
  ].join('\n')

  await mkdir(dirname(generatedPath), { recursive: true })
  await writeFile(generatedPath, output)

  const registry = await motionRegistry()
  const registryOutput = [
    '/**',
    ' * Generated from contracts/story-presentation/motion-capabilities.json.',
    ' * Do not edit this file by hand. Run `pnpm contracts:generate`.',
    ' */',
    '',
    `export const MOTION_CAPABILITY_REGISTRY_VERSION = '${registry.value.version}' as const`,
    `export const MOTION_CAPABILITY_REGISTRY_HASH = '${registry.hash}' as const`,
    `export const MOTION_ROLE_PROFILE_DECLARATIONS = ${JSON.stringify(registry.value.profileDeclarations, null, 2)} as const`,
    `export const MOTION_CAPABILITIES = ${JSON.stringify(registry.value.capabilities, null, 2)} as const`,
    '',
    'export type MotionCapability = (typeof MOTION_CAPABILITIES)[number]',
    "export type MotionRoleClassification = MotionCapability['roleClassifications'][number]",
    "export type MotionSemanticRole = MotionRoleClassification['role']",
    'export type MotionRoleTargetProfile = keyof typeof MOTION_ROLE_PROFILE_DECLARATIONS',
    '',
  ].join('\n')
  await writeFile(generatedMotionCapabilitiesPath, registryOutput)
}

async function copyContract(destination) {
  await rm(destination, { recursive: true, force: true })
  await mkdir(dirname(destination), { recursive: true })
  await cp(contractRoot, destination, { recursive: true })
}

async function writeChecksum(destination) {
  const files = [
    'version.json',
    'story-presentation.schema.json',
    'scene-presentation.schema.json',
    'modes/scrolly.schema.json',
    'modes/motion.schema.json',
    'motion-capability-registry.schema.json',
    'motion-capabilities.json',
  ]
  const hash = createHash('sha256')
  for (const file of files) {
    hash.update(file)
    hash.update(await readFile(resolve(contractRoot, file)))
  }
  await writeFile(resolve(destination, 'contract.sha256'), `${hash.digest('hex')}\n`)
}

async function sync() {
  const backendRoot = resolve(repositoryRoot, process.env.CMS_BACKEND_ROOT || '../cms-backend')
  const componentsRoot = resolve(
    repositoryRoot,
    process.env.CMS_STORY_COMPONENTS_ROOT || '../cms-story-components',
  )
  const backendDestination = resolve(backendRoot, 'resources/contracts/story-presentation')
  const componentsDestination = resolve(componentsRoot, 'contracts/story-presentation')

  await copyContract(backendDestination)
  await writeChecksum(backendDestination)
  await copyContract(componentsDestination)
  await writeChecksum(componentsDestination)
}

const command = process.argv[2]
if (command === 'generate') {
  await generate()
} else if (command === 'sync') {
  await sync()
} else {
  throw new Error('Expected `generate` or `sync`.')
}
